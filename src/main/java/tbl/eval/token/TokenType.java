package tbl.eval.token;

public enum TokenType {
    NUM, // number
    PLUS, // +
    DOUBLE_PLUS, // ++
    MINUS, // -
    DOUBLE_MINUS, // --
    MUL, // *
    DIV, // /
    REM, // %
    LPAREN, // (
    RPAREN, // )
    VAR, // variable
    ASSIGN, // =
    ADD_ASSIGN, // +=
    SUB_ASSIGN, // -=
    MUL_ASSIGN, // *=
    DIV_ASSIGN, // /=
    REM_ASSIGN, // %=
    EOF // end of input

}
